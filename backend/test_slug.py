#!/usr/bin/env python
"""
Тестовый скрипт для проверки генерации slug из названий товаров
"""
import re

def transliterate_russian(text):
    """Транслитерация русских символов в латиницу"""
    translit_dict = {
        'а': 'a', 'б': 'b', 'в': 'v', 'г': 'g', 'д': 'd', 'е': 'e', 'ё': 'yo',
        'ж': 'zh', 'з': 'z', 'и': 'i', 'й': 'y', 'к': 'k', 'л': 'l', 'м': 'm',
        'н': 'n', 'о': 'o', 'п': 'p', 'р': 'r', 'с': 's', 'т': 't', 'у': 'u',
        'ф': 'f', 'х': 'h', 'ц': 'ts', 'ч': 'ch', 'ш': 'sh', 'щ': 'sch', 'ъ': '',
        'ы': 'y', 'ь': '', 'э': 'e', 'ю': 'yu', 'я': 'ya',
        'А': 'A', 'Б': 'B', 'В': 'V', 'Г': 'G', 'Д': 'D', 'Е': 'E', 'Ё': 'Yo',
        'Ж': 'Zh', 'З': 'Z', 'И': 'I', 'Й': 'Y', 'К': 'K', 'Л': 'L', 'М': 'M',
        'Н': 'N', 'О': 'O', 'П': 'P', 'Р': 'R', 'С': 'S', 'Т': 'T', 'У': 'U',
        'Ф': 'F', 'Х': 'H', 'Ц': 'Ts', 'Ч': 'Ch', 'Ш': 'Sh', 'Щ': 'Sch', 'Ъ': '',
        'Ы': 'Y', 'Ь': '', 'Э': 'E', 'Ю': 'Yu', 'Я': 'Ya'
    }
    
    result = []
    for char in text:
        result.append(translit_dict.get(char, char))
    return ''.join(result)

def create_slug(text):
    """Создание slug из текста с поддержкой кириллицы"""
    # Транслитерируем русские символы
    text = transliterate_russian(text)
    # Заменяем точки и другие символы на дефисы
    text = re.sub(r'[^\w\s-]', '-', text)
    # Заменяем пробелы на дефисы
    text = re.sub(r'[\s_]+', '-', text)
    # Убираем множественные дефисы
    text = re.sub(r'-+', '-', text)
    # Убираем дефисы в начале и конце
    text = text.strip('-')
    # Приводим к нижнему регистру
    return text.lower()

# Тестовые примеры
test_names = [
    "SPECTRAL SOFT LIGHT Шпатлевка многофункциональная 1л.",
    "Koch Chemie Полироль для пластика 500мл",
    "Автошампунь концентрат 5л.",
    "Очиститель стёкол 750мл",
]

print("Тестирование генерации slug:\n")
for name in test_names:
    slug = create_slug(name)
    print(f"Название: {name}")
    print(f"Slug:     {slug}")
    print("-" * 80)
